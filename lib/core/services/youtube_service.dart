  // 모든 동영상 목록 가져오기
  static List<YouTubeVideo> getAllVideos() {
    return List.from(_videos);
  }

  // 카테고리별 동영상 목록 가져오기
  static List<YouTubeVideo> getVideosByCategory(String category) {
    return _videos.where((video) => video.category == category).toList();
  }

  // 최신 동영상 목록 가져오기 (최근 10개)
  static List<YouTubeVideo> getRecentVideos({int limit = 10}) {
    final sortedVideos = List<YouTubeVideo>.from(_videos);
    sortedVideos.sort((a, b) => b.dateAdded.compareTo(a.dateAdded));
    return sortedVideos.take(limit).toList();
  }

  // 동영상 검색
  static List<YouTubeVideo> searchVideos(String query) {
    return _videos
        .where((video) =>
            video.title.toLowerCase().contains(query.toLowerCase()) ||
            video.notes.toLowerCase().contains(query.toLowerCase()))
        .toList();
  }
